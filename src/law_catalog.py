def resolve_catalog():
    """返回 [(Path, effective_date), ...]，只包含本地实际存在的文件。

    缺失文件会被跳过并提示（方便用户只克隆了部分 Laws 的场景）。
    """
    resolved = []
    missing = []
    for rel, eff in LAW_CATALOG:
        p = LAWS_DIR / rel
        if p.exists():
            resolved.append((p, eff))
        else:
            missing.append(rel)
    return resolved, missing


def civil_code_dir() -> Path:
    return LAWS_DIR / CIVIL_CODE_DIR
